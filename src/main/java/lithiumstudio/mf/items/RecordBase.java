public class RecordBase extends MusicDiscItem {
    private String key;
    public RecordBase(int comparatorOutput, SoundEvent sound, Settings settings, String key) {
        super(comparatorOutput, sound, settings.maxCount(1));
        this.key = key;
    }

    @Override
    public void appendTooltip(ItemStack stack, @Nullable World world, List<Text> tooltip, TooltipContext context) {
        tooltip.add(new TranslatableText("item.mf."+key+".tooltip").formatted(Formatting.BLUE));
    }
}
